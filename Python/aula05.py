"""Tipos de dados bool (bolean)
ao questionar algo em um programa
só existe duas respostas possiveis
True:Sim ou False:Não
Existe varios operadores para "questionar".
dentre eles o ==, é o operador lógico
que questiona se um valor é igual a outro."""

print("10 é igual a 10?:", 10 == 10) #Sim ou True (Verdadeiro)
print("10 é igual a 9? :", 10 == 9) #Não ou False (Falso)

print(1, type(10 == 10), sep="º ")
print(2, type(10 == 9), sep="º ")
