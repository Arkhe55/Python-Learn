#Tipos int e float
#Int -> Números inteiros
#Tipo int representa qualquer número
#possitivo ou negativo, int sem sinal é considerado possitivo

print(1, 50, sep="º ")
print(2, -50, sep="º ")
print(3, 0, sep="º ")

#float -> Números com ponto flutuante
#O tipo float representa qualuqer número
#possitivo negativo com ponto flutuante
#float sem sinal é considerado possitivo

print(4, 5.3, sep="º ")
print(5, -5.23, sep="º ")
print(6, 0.23, sep="º ")

#A função type mostra o tipo que o Python
#inseriu ao valor

print(type(5))
print(type(0.5))
print(type("Gabriel"))
