"""Fatiamento de string
indice[] Possitivo 012345678
Negativo -987654321 = Olá Mundo (Espaço também é contado)
Fatiamento [i:f:p] [::]
Função len de lenght de comprimento denomina quantidade de uma string
"""

variavel=("Olá Mundo")

print(variavel[4:])
print(variavel[-6:])
print(variavel[0:9:1])
print(variavel[::-1])
print(len(variavel))
